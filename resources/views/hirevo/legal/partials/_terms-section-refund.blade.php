<p>All subscription fees, onboarding charges, activation fees, service fees, consulting fees, and other applicable charges shall be payable in advance unless otherwise agreed in writing.</p>
<p>Applicable taxes, including GST, shall be charged separately wherever required under applicable law.</p>

<hr class="hirevo-legal-apna__divider">

<h3>3. Refund Policy</h3>
<p>Due to the nature of digital services, software access, onboarding efforts, resource allocation, and platform provisioning, payments made towards Hirevoo services are generally non-refundable once the service has been activated or access has been granted.</p>
<p>However, Hirevoo may review refund requests on a case-by-case basis in exceptional circumstances.</p>

<hr class="hirevo-legal-apna__divider">

<h3>4. Eligible Refund Requests</h3>
<p>Refund requests may be considered under the following circumstances:</p>
<h4>4.1 Duplicate Payment</h4>
<p>Where a customer has accidentally made multiple payments for the same service.</p>
<h4>4.2 Incorrect Billing</h4>
<p>Where an incorrect amount has been charged due to a technical or billing error attributable to Hirevoo.</p>
<h4>4.3 Non-Provision of Service</h4>
<p>Where Hirevoo is unable to activate or provide the purchased service due to reasons solely attributable to Hirevoo.</p>
<p>Any approved refund shall be processed through the original payment method subject to applicable banking timelines.</p>

<hr class="hirevo-legal-apna__divider">

<h3>5. Non-Refundable Circumstances</h3>
<p>Refunds shall generally not be provided for:</p>
<ul>
    <li>Change in hiring requirements</li>
    <li>Internal hiring freeze</li>
    <li>Business restructuring</li>
    <li>Budget constraints</li>
    <li>Lack of platform usage</li>
    <li>Dissatisfaction arising from recruitment outcomes</li>
    <li>Candidate availability issues</li>
    <li>Candidate response rates</li>
    <li>Offer rejections</li>
    <li>Candidate dropouts</li>
    <li>Delays caused by customer-side processes</li>
    <li>Failure to use available platform features</li>
    <li>Early discontinuation of hiring activities</li>
</ul>

