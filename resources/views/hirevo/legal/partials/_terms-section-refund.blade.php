<p>Hirevoo provides technology-enabled recruitment and hiring support solutions.</p>
<p>The Customer acknowledges that Hirevoo does not guarantee:</p>
<ul>
    <li>Candidate applications</li>
    <li>Candidate availability</li>
    <li>Candidate quality</li>
    <li>Interviews</li>
    <li>Offer acceptance</li>
    <li>Candidate joining</li>
    <li>Hiring success</li>
    <li>Employee retention</li>
</ul>
<p>Hiring outcomes depend upon several factors outside Hirevoo’s control, including market conditions, job specifications, compensation structures, employer responsiveness, candidate preferences, and hiring practices.</p>
