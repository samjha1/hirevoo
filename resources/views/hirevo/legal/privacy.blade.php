This Privacy Policy explains in clear, transparent language exactly how Hirevoo collects, uses, shares, and protects your personal data. We are committed to treating your data with respect and to conducting our business honestly and openly.

By using the Hirevoo platform, you acknowledge that you have read and understood this Privacy Policy and consent to the data practices described herein.

1. INTRODUCTION
1.1 Who We Are
Hirevoo Pvt. Ltd. ("Hirevoo", "we", "us", or "our") is a company incorporated under the laws of India, operating the Hirevoo Career Acceleration Platform ("Platform") accessible at www.hirevoo.in and through associated mobile applications and APIs.
Hirevoo is a technology platform that provides job discovery, resume analysis, AI-based match scoring, referral facilitation, career consultation, upskill recommendations, and employer hiring tools. We are the Data Fiduciary (as defined under the Digital Personal Data Protection Act, 2023) with respect to personal data processed through our Platform.

1.2 Scope of This Policy
This Privacy Policy applies to:
All registered Users of the Platform, including Candidates, Employees (Referrers), and Employers.
Visitors who browse the Platform without registering.
Partners, affiliates, and service providers who interact with the Platform.
This Policy governs all personal data collected, used, stored, shared, and processed by Hirevoo in connection with the Platform and its Services.

1.3 Legal Framework
Hirevoo's data practices are designed to comply with:
The Digital Personal Data Protection Act, 2023 (India) ("DPDP Act") and rules thereunder.
The Information Technology Act, 2000 and the IT (Amendment) Act, 2008 (India).
The Information Technology (Reasonable Security Practices and Procedures and Sensitive Personal Data or Information) Rules, 2011 ("SPDI Rules").
Applicable international data protection principles, including those derived from the General Data Protection Regulation (EU) ("GDPR"), to the extent they may apply to our operations.

2. PERSONAL DATA WE COLLECT
We collect the following categories of personal data:

Category - Data Points
Personal Information - Full name, email address, mobile number, residential city/state, profile photograph (optional).
Professional Data - Resume and CV documents, current and previous job titles, employers, employment history, educational qualifications, degrees, certifications, skills, salary expectations, notice period, and career preferences.
Usage and Behavioural Data - Pages visited, job listings viewed, referral requests submitted, buttons clicked, features used, time spent on sections, search queries, scroll behaviour, and in-app navigation patterns.
Communication Data - Records of calls made to/from Hirevoo's sales and support teams, chat messages on the Platform, email correspondence with Hirevoo, and WhatsApp/SMS interactions with Hirevoo's authorised representatives.
Device and Technical Data - IP address, browser type and version, operating system, device type and model, screen resolution, referring URLs, session identifiers, and access timestamps.
Payment Data - Transaction amounts, payment dates, payment method type (e.g., card, UPI, net banking), and transaction reference numbers. We do not directly store full card numbers, CVV codes, or bank account credentials. Payments are processed through PCI-DSS compliant third-party processors.
Profile Interaction Data - Referral request history, employer interactions, match score history, applications tracked, consultation session records, and course/upskill recommendations received.
Inferred and Derived Data - AI-generated match scores, skill gap analyses, career readiness assessments, and other probabilistic indicators derived from your profile and behaviour on the Platform.

SENSITIVE PERSONAL DATA
To the extent that any information you voluntarily provide constitutes Sensitive Personal Data or Information (SPDI) as defined under the SPDI Rules (e.g., financial information, health data), Hirevoo will seek your explicit consent before processing such data.

